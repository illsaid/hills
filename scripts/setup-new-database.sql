-- ---------------------------------------------------------------------------
-- 4. FireScore (real estate page): PostGIS zones + point lookup RPC
-- ---------------------------------------------------------------------------
CREATE EXTENSION IF NOT EXISTS postgis;

CREATE TABLE IF NOT EXISTS fire_zones (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    source TEXT NOT NULL,
    zone_class TEXT NOT NULL,
    geom GEOGRAPHY(MULTIPOLYGON, 4326) NOT NULL,
    updated_at TIMESTAMPTZ DEFAULT NOW(),
    raw JSONB
);

CREATE INDEX IF NOT EXISTS idx_fire_zones_geom ON fire_zones USING GIST (geom);
CREATE INDEX IF NOT EXISTS idx_fire_zones_class ON fire_zones (zone_class);

ALTER TABLE fire_zones ENABLE ROW LEVEL SECURITY;

DO $$ BEGIN
  CREATE POLICY "Public read access" ON fire_zones FOR SELECT USING (true);
EXCEPTION WHEN duplicate_object THEN NULL; END $$;
DO $$ BEGIN
  CREATE POLICY "Service role full access" ON fire_zones USING (true) WITH CHECK (true);
EXCEPTION WHEN duplicate_object THEN NULL; END $$;

CREATE OR REPLACE FUNCTION get_fire_zone(lat DOUBLE PRECISION, lon DOUBLE PRECISION)
RETURNS TABLE (zone_class TEXT, source TEXT)
LANGUAGE plpgsql
AS $$
BEGIN
    RETURN QUERY
    SELECT f.zone_class, f.source
    FROM fire_zones f
    WHERE ST_Covers(f.geom, ST_SetSRID(ST_MakePoint(lon, lat), 4326)::geography)
    ORDER BY
        CASE f.zone_class
            WHEN 'VERY_HIGH' THEN 1
            WHEN 'HIGH' THEN 2
            ELSE 3
        END
    LIMIT 1;
END;
$$;

