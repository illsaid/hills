const { createClient } = require('@supabase/supabase-js');
require('dotenv').config({ path: '.env.local' });
require('dotenv').config();

const supabaseUrl = process.env.NEXT_PUBLIC_SUPABASE_URL;
const supabaseKey = process.env.SUPABASE_SERVICE_ROLE_KEY || process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY;

if (!supabaseUrl || !supabaseKey) {
    console.error('❌ Missing Supabase credentials');
    process.exit(1);
}

const supabase = createClient(supabaseUrl, supabaseKey);

async function createTables() {
    console.log('🏗️  Creating historical trend tables...');

    // 1. Crime Trends Table
    const createCrimeTrends = `
    CREATE TABLE IF NOT EXISTS crime_trends (
        id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        date DATE NOT NULL,
        district TEXT NOT NULL,
        category TEXT NOT NULL,
        count INT NOT NULL,
        rolling_8wk_avg FLOAT,
        status TEXT,
        sources JSONB,
        created_at TIMESTAMPTZ DEFAULT NOW(),
        UNIQUE(date, district, category)
    );
    `;

    // 2. Permit Velocity Table
    const createPermitVelocity = `
    CREATE TABLE IF NOT EXISTS permit_velocity (
        id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        week_ending DATE NOT NULL,
        zip TEXT NOT NULL,
        count INT NOT NULL,
        avg_value FLOAT,
        yoy_change FLOAT,
        created_at TIMESTAMPTZ DEFAULT NOW(),
        UNIQUE(week_ending, zip)
    );
    `;

    // Execute via RPC or direct query if available? 
    // Supabase JS client doesn't support raw SQL execution directly on the client unless a stored procedure exists.
    // However, for this environment, we might assume the user (me) has direct access or we can try to use the logic 
    // from previous migrations if they exist. 
    // Checking previous 'migrate-schema.js' might be wise, but I'll try to just inform the user 
    // or use a "rpc" call if I had one. 

    // Actually, since I am "Antigravity", I can't easily run DDL via supabase-js client without the Service Role 
    // AND a function to execute SQL. 
    // BUT! I can use the `postgres` tool if I had it, OR I can simulate it by asking the user to run it.
    // Wait, `migrate-schema.js` exists in the file list I saw earlier. Let me check how it works.

    const sql = createCrimeTrends + '\n' + createPermitVelocity;

    console.log('🔄 Attempting migration via RPC...');

    const { error } = await supabase.rpc('exec_sql', { sql });

    if (error) {
        console.error('❌ RPC failed (Function exec_sql likely missing), falling back to SQL Editor hint:', error.message);
        console.log('\n⚠️  ACTION REQUIRED: Run the following SQL in your Supabase SQL Editor:');
        console.log(sql);
    } else {
        console.log('✅ Tables created successfully via RPC.');
    }
}
